namespace Tempo.Blazor.Components.Navigation;

/// <summary>
/// Visual variant for the tab strip.
/// </summary>
public enum TabVariant
{
    /// <summary>Underline indicator below active tab (default).</summary>
    Line,

    /// <summary>Pill/rounded background on active tab.</summary>
    Pill,

    /// <summary>Enclosed card-style tabs with border.</summary>
    Enclosed
}
